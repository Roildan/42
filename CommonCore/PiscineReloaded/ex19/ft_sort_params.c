void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
}

int		ft_strcmp(char *s1, char *s2)
{
	while (*s1 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}

void	ft_swap(char **s1, char **s2)
{
	char *temp;

	temp = *s1;
	*s1 = *s2;
	*s2 = temp;
}

int		main(int ac, char **av)
{
	int i;
	int j;

	if (ac == 1)
		return (0);
	i = ac;
	while (--i > 1)
	{
		j = 0;
		while (++j < i)
			if (ft_strcmp(av[j + 1], av[j]) < 0)
				ft_swap(&av[j + 1], &av[j]);
	}
	i = 1;
	while (i < ac)
	{
		ft_putstr(av[i++]);
		ft_putchar('\n');
	}
	return (0);
}
